        public string sonisim = "";
        public string sonsoyisim = "";
        public string sonhesapismi = "";
        public void randomisim()
        {
            Random rndisim = new Random();
            Random rndsoyisim = new Random();
            Random rndhesapismi = new Random();
            int isimrnd = rndisim.Next(0, isim.Count);
            int soyisimrnd = rndsoyisim.Next(0, soyisim.Count);
            int hesapisimrnd = rndhesapismi.Next(100, 1000);

            sonisim = isim[isimrnd].ToString();
            sonsoyisim = soyisim[soyisimrnd].ToString();
            sonhesapismi = TurkishCharacterToEnglish(sonisim.ToLower()) + " " + TurkishCharacterToEnglish(sonsoyisim.ToLower()) + hesapisimrnd;
            sonhesapismi.Replace(" ", "");
            Console.WriteLine(sonisim + " " + sonsoyisim + " " + sonhesapismi);
        }

        public string TurkishCharacterToEnglish(string text)
        {
            char[] turkishChars = { 'ı', 'ğ', 'İ', 'Ğ', 'ç', 'Ç', 'ş', 'Ş', 'ö', 'Ö', 'ü', 'Ü' };
            char[] englishChars = { 'i', 'g', 'I', 'G', 'c', 'C', 's', 'S', 'o', 'O', 'u', 'U' };

            // Match chars
            for (int i = 0; i < turkishChars.Length; i++)
                text = text.Replace(turkishChars[i], englishChars[i]);

            return text;
        }

